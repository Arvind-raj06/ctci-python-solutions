def sortStack ( stack ): 
    tmpStack = createStack() 
    while(isEmpty(stack) == False): 
        tmp = top(stack) 
        pop(stack)
        while(isEmpty(tmpStack) == False and int(top(tmpStack)) > int(tmp)): 
            push(stack,top(tmpStack)) 
            pop(tmpStack) 
        push(tmpStack,tmp) 
    return tmpStack
    
def push( stack, item ): 
    stack.append( item ) 
  
def top( stack ): 
    p = len(stack) 
    return stack[p-1] 
    
def pop( stack ): 
    if(len(stack)==0): 
        print("Stack Underflow ") 
        exit(1)
    return stack.pop() 
